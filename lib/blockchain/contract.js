export const getContract = (signer) => {
  return new ethers.Contract(
    CONTRACT_ADDRESS,
    CONTRACT_ABI,
    signer
  );
};

export const mintCharacter = async (signer, quantity = 1) => {
  try {
    const contract = getContract(signer);
    const mintPrice = await contract.mintPrice();
    const totalPrice = mintPrice.mul(quantity);

    const tx = await contract.mint(quantity, {
      value: totalPrice
    });

    const receipt = await tx.wait();
    
    // Get the token ID from the Transfer event
    const transferEvent = receipt.events?.find(e => e.event === 'Transfer');
    const tokenId = transferEvent?.args?.tokenId?.toString();

    return {
      success: true,
      hash: receipt.transactionHash,
      tokenId
    };
  } catch (error) {
    console.error('Error minting character:', error);
    return {
      success: false,
      error: error.message,
    };
  }
};

export const setCollectionURI = async (signer, baseURI) => {
  try {
    const contract = getContract(signer);
    const tx = await contract.setBaseURI(baseURI);
    await tx.wait();
    return {
      success: true,
    };
  } catch (error) {
    console.error('Error setting base URI:', error);
    return {
      success: false,
      error: error.message,
    };
  }
};

export const getTokenURI = async (signer, tokenId) => {
  try {
    const contract = getContract(signer);
    const uri = await contract.tokenURI(tokenId);
    return {
      success: true,
      uri,
    };
  } catch (error) {
    console.error('Error getting token URI:', error);
    return {
      success: false,
      error: error.message,
    };
  }
};